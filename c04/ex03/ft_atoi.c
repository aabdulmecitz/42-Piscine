int	ft_atoi(char *str)
{
	int	i;
	int	n_c;
	int	val;

	i = 0;
	n_c = 1;
	val = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n_c *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		val = (val * 10) + (str[i] - 48);
		i++;
	}
	return (val * n_c);
}
